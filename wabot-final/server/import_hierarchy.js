// ─── HTTP helper ─────────────────────────────────────────────────────────────
function apiRequest(method, path, body, token) {
  return new Promise((resolve, reject) => {
    const data = body ? JSON.stringify(body) : null;
    const headers = { 'Content-Type': 'application/json' };
    if (token) headers['Authorization'] = 'Bearer ' + token;
    if (data) headers['Content-Length'] = Buffer.byteLength(data);
    const req = https.request(
      { hostname: API_HOST, port: 443, path, method, headers },
      res => {
        let raw = '';
        res.on('data', d => raw += d);
        res.on('end', () => {
          try { resolve({ status: res.statusCode, data: JSON.parse(raw) }); }
          catch { resolve({ status: res.statusCode, data: raw }); }
        });
      }
    );
    req.on('error', reject);
    if (data) req.write(data);
    req.end();
  });
}

function delay(ms) { return new Promise(r => setTimeout(r, ms)); }

// ─── Main ─────────────────────────────────────────────────────────────────────
(async () => {
  // 1. Login
  console.log('🔐 מתחבר...');
  const loginRes = await apiRequest('POST', '/api/auth/login', { email: EMAIL, password: PASSWORD });
  if (!loginRes.data.token) { console.error('Login failed:', loginRes.data); process.exit(1); }
  const token = loginRes.data.token;
  console.log('✅ מחובר\n');

  // 2. Load existing shortcuts to avoid duplicates
  console.log('📥 טוען קיצורים קיימים...');
  const existingRes = await apiRequest('GET', '/api/areas/shortcuts?limit=500', null, token);
  const existing = new Set((existingRes.data.data || []).map(x => x.shortName));
  console.log(`   קיימים: ${existing.size}\n`);

  // 3. Import shortcuts
  console.log('🔑 מוסיף קיצורים...');
  let sAdded = 0, sSkipped = 0, sFailed = 0;
  for (const [shortName, fullName] of Object.entries(SHORTCUTS)) {
    if (existing.has(shortName)) { process.stdout.write(`  ⏭  ${shortName}\n`); sSkipped++; continue; }
    const r = await apiRequest('POST', '/api/areas/shortcuts', { shortName, fullName }, token);
    if (r.status === 201 || r.status === 200) {
      process.stdout.write(`  ✓ ${shortName} → ${fullName}\n`); sAdded++;
    } else {
      process.stdout.write(`  ✗ ${shortName}: ${JSON.stringify(r.data)}\n`); sFailed++;
    }
    await delay(30);
  }

  // 4. Load existing related areas
  console.log('\n🏙️  מוסיף קשרי שכונות-ערים (Related Areas)...');
  const relRes = await apiRequest('GET', '/api/areas/related?limit=100', null, token);
  const existingRel = new Set((relRes.data.data || []).map(x => x.main));

  let rAdded = 0, rUpdated = 0, rFailed = 0;
  for (const [cityName, cityData] of Object.entries(CITY_HIERARCHY)) {
    const { aliases = [], neighborhoods = [] } = cityData;
    // related = all aliases + all neighborhoods
    const related = [...new Set([...aliases, ...neighborhoods])];
    if (related.length === 0) {
      console.log(`  ⏭  ${cityName} (אין שכונות)`); continue;
    }
    const isUpdate = existingRel.has(cityName);
    const r = await apiRequest('POST', '/api/areas/related', { main: cityName, related }, token);
    if (r.status === 200 || r.status === 201) {
      console.log(`  ✓ ${cityName}: ${related.length} קשרים (${isUpdate ? 'עודכן' : 'נוסף'})`);
      isUpdate ? rUpdated++ : rAdded++;
    } else {
      console.log(`  ✗ ${cityName}: ${JSON.stringify(r.data)}`); rFailed++;
    }
    await delay(50);
  }

  console.log('\n📊 סיכום:');
  console.log(`   קיצורים: ✅ ${sAdded} נוספו | ⏭  ${sSkipped} קיימים | ❌ ${sFailed} נכשלו`);
  console.log(`   שכונות:  ✅ ${rAdded} נוספו | 🔄 ${rUpdated} עודכנו | ❌ ${rFailed} נכשלו`);
  console.log('\n✅ סיום!');
})();
