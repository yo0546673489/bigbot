// ─── HTTP helper ─────────────────────────────────────────────────────────────

function apiRequest(method, path, body, token) {
  return new Promise((resolve, reject) => {
    const data = body ? JSON.stringify(body) : null;
    const opts = {
      hostname: API_HOST,
      path,
      method,
      headers: {
        'Content-Type': 'application/json',
        ...(token ? { Authorization: 'Bearer ' + token } : {}),
        ...(data ? { 'Content-Length': Buffer.byteLength(data) } : {}),
      },
    };
    const req = https.request(opts, res => {
      let raw = '';
      res.on('data', c => raw += c);
      res.on('end', () => {
        try { resolve({ status: res.statusCode, body: JSON.parse(raw) }); }
        catch { resolve({ status: res.statusCode, body: raw }); }
      });
    });
    req.on('error', reject);
    if (data) req.write(data);
    req.end();
  });
}

async function login() {
  const r = await apiRequest('POST', '/api/auth/login', { email: EMAIL, password: PASSWORD });
  if (!r.body.token) throw new Error('Login failed: ' + JSON.stringify(r.body));
  return r.body.token;
}

async function loadExisting(token) {
  const existing = new Set();
  let page = 1;
  while (true) {
    const r = await apiRequest('GET', `/api/areas/shortcuts?page=${page}&limit=200`, null, token);
    (r.body.data || []).forEach(d => existing.add(d.shortName));
    if (page >= (r.body.totalPages || 1)) break;
    page++;
  }
  return existing;
}

async function addOne(token, shortName, fullName) {
  const r = await apiRequest('POST', '/api/areas/shortcuts', { shortName, fullName }, token);
  return r.status < 300;
}

// ─── Main ────────────────────────────────────────────────────────────────────

async function main() {
  console.log('🔐 מתחבר לשרת...');
  const token = await login();
  console.log('✅ מחובר');

  console.log('📥 טוען פריטים קיימים מה-DB...');
  const existing = await loadExisting(token);
  console.log(`   קיימים: ${existing.size} פריטים`);

  let added = 0, skipped = 0, failed = 0;

  // הוסף קיצורים
  console.log('\n🔑 מוסיף קיצורים...');
  for (const { shortName, fullName } of SHORTCUTS) {
    if (existing.has(shortName)) { skipped++; continue; }
    const ok = await addOne(token, shortName, fullName);
    if (ok) { added++; existing.add(shortName); process.stdout.write(`  ✓ ${shortName} → ${fullName}\n`); }
    else { failed++; process.stdout.write(`  ✗ ${shortName}\n`); }
  }

  // הוסף ערים
  console.log('\n🏙️  מוסיף ערים...');
  for (const name of CITIES) {
    const n = name.trim();
    if (!n || existing.has(n)) { skipped++; continue; }
    const ok = await addOne(token, n, n);
    if (ok) { added++; existing.add(n); process.stdout.write(`  ✓ ${n}\n`); }
    else { failed++; process.stdout.write(`  ✗ ${n}\n`); }
    // Small delay to avoid overwhelming the server
    await new Promise(r => setTimeout(r, 30));
  }

  console.log(`\n📊 סיכום:`);
  console.log(`   ✅ נוספו:  ${added}`);
  console.log(`   ⏭️  קיימים: ${skipped}`);
  console.log(`   ❌ נכשלו:  ${failed}`);
  console.log('\nסיום!');
}

main().catch(console.error);
